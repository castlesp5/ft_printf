#include "../printf.h"

int	ft_putuns(unsigned int n, int i)
{
	char	x;

	if (n >= 10)
		i = ft_putuns(n / 10, i);
	x = (n % 10) + 48;
	i += write(1, &x, 1);
	return (i);
}
